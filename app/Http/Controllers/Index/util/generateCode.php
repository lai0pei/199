namespace App\Http\Controllers\Index\util;

use Illuminate\Support\Facades\Cache;

trait GenerateCode
{
    public function getCode($mobile)
    {
        $key = $mobile;
        Cache::forget($key);
        $length = 5;
        $code = '';
        for ($i = 0; $i < $length; ++$i) {
            $code .= random_int(0, 9);
        }

        Cache::put($key, $code);

        return $code;
    }
}
